#include <stdio.h>

/**
* add - add two integers
* @a: first unit
* @b: second unit
* Return: addition
*/

int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtract two integers
* @a: first int
* @b: second int
* Return: subtraction
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
* add - multiplication of two integers
* @a: first unit
* @b: second unit
* Return: multiply them
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
* div - divide two integers
* @a: first unit
* @b: second unit
* Return: division
*/

int div(int a, int b)
{
	return (a / b);
}

/**
* mod - mod of two integers
* @a: first unit
* @b: second unit
* Return: final answer
*/

int mod(int a, int b)
{
	return (a % b);
}
